package EnumBasics;

public class EnumBasics {
	// Q:- What is Enum?
	// A: If we want to represent a Group Of named Constants then we should go for
	// enum,Here Semi Colon is Optional
	// Ex:(1)
	enum Month {

		JAN, FEB, MAR, APR, MAY, JUN, JULU, AUG, SEP, OCT, NOV, DEC

	}

	// Ex(2):
	enum Beer {

		KF, KO, RC, FO;
	}

	// Enum Concept Introdued in 1.5 Version
	// The Main Objective of enum is to define our own Data Types(Enumerated
	// DataTypes)

}
